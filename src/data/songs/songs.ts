import { Song, Note, LyricLine, midiToFrequency } from '@/types/game';

// Helper to create notes
function createNote(
  id: string,
  midi: number,
  startTime: number,
  duration: number,
  lyric: string,
  isBonus = false,
  isGolden = false
): Note {
  return {
    id,
    pitch: midi,
    frequency: midiToFrequency(midi),
    startTime,
    duration,
    lyric,
    isBonus,
    isGolden,
  };
}

// Helper to create lyric line with notes
function createLine(
  id: string,
  text: string,
  startTime: number,
  endTime: number,
  notes: Note[]
): LyricLine {
  return { id, text, startTime, endTime, notes };
}

